    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblSupplier.getModel();
        dtm.setRowCount(0);
        for(Supplier supplier : ecoSystem.getSupplierDirectory().getSupplierDirectory()){
            Object [] row = new Object[3];
            row[0] = supplier;
            row[1] = supplier.getAddress();
            row[2] = supplier.getPhoneNumber();
            dtm.addRow(row);
        }
    }

