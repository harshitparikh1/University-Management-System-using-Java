    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDonation.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row from table first", "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Supplier supplier = (Supplier) tblSupplier.getValueAt(selectedRow, 0);
        UserAccount account1 = ecoSystem.getUserAccountDirectory().findUserAccount(supplier.getUserName());
        ecoSystem.getUserAccountDirectory().deleteUserAccount(account1);

        supplierDirectory.removeSupplier(supplier);
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

